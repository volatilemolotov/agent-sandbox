import time
import random
import ray

# ==============================================================================
# Agentic Reinforcement Learning with Ray and K8s Agent Sandbox
# 
# Architecture: Direct-Routed Decoupled Sandbox Model
# 1. Trusted Control Plane: Ray Head and Workers run on standard K8s nodes.
# 2. Untrusted Data Plane: AI code executes in isolated gVisor Sandboxes.
# 3. Direct Routing: Ray Workers hit gVisor Sandbox pod IPs directly for max speed.
# ==============================================================================

@ray.remote
class SecureEnvironmentActor:
    """
    A secure sandboxed environment runner using the official Agent Sandbox SDK.
    This actor physically runs on the trusted Ray worker node, but it proxies all 
    untrusted execution to an isolated gVisor pod over the internal cluster network.
    """
    def __init__(self, env_id: str):
        self.env_id = env_id
        from k8s_agent_sandbox import SandboxClient
        from k8s_agent_sandbox.models import SandboxInClusterConnectionConfig
        
        print(f"[{self.env_id}] - Initializing GKE Sandbox Client natively In-Cluster...")
        
        # By using SandboxInClusterConnectionConfig with `use_pod_ip=True`, we bypass 
        # the external Gateway and internal Routers. The SDK resolves the internal K8s 
        # pod IP of the sandbox and routes traffic directly via pod-to-pod 
        # communication.
        connection_config = SandboxInClusterConnectionConfig(
            use_pod_ip=True,
            server_port=8888 
        )
        
        self.client = SandboxClient(connection_config=connection_config, cleanup=True)
        
        print(f"[{self.env_id}] - Requesting sandbox from WarmPool...")
        t_claim_start = time.time()
        
        # Instead of asking K8s to boot a new Sandbox and pull images from scratch, we claim a pod 
        # that the SandboxWarmPool has already booted. This reduces provisioning time from 
        # ~60 seconds down to <200 milliseconds.
        self.sandbox = self.client.create_sandbox(
            template="ray-native-template",
            warmpool="ray-native-pool"
        )
        
        self.t_adopted = time.time() 
        claim_latency = self.t_adopted - t_claim_start
        print(f"[{self.env_id}] -> Sandbox '{self.sandbox.claim_name}' adopted in {claim_latency:.3f}s")

    def step(self, action: int):
        """
        Executes a single step in the RL environment.
        The action is untrusted code generated by the agent.
        """
        # Start the total step timer
        t_step_start = time.time()
        
        # Simulate the agent generating a Python script based on the action space
        code = f"import random; print({action} + 10)"
        
        # 1. Upload Phase: Push the script to the isolated gVisor pod
        t_upload_start = time.time()
        self.sandbox.files.write("agent_action.py", code)
        upload_latency = time.time() - t_upload_start
        
        # 2. Execution Phase: Command the sandbox to run the code
        t_exec_start = time.time()
        res = self.sandbox.commands.run("python agent_action.py", timeout=5)
        exec_latency = time.time() - t_exec_start
        
        # 3. Observation & Reward Extraction
        # In a real environment, this would parse the stdout/stderr of the execution
        observation = [random.random(), random.random(), random.random(), random.random()]
        reward = -1.0
        
        # If the code executed successfully, parse the output as the reward
        if res.exit_code == 0:
            try:
                reward = float(res.stdout.strip())
            except (TypeError, ValueError):
                pass
                
        # Simulate environment termination logic
        done = random.choice([False, False, False, True])
        
        # Calculate total end-to-end step time
        step_total = time.time() - t_step_start
        print(f"[{self.env_id}] -> Step completed in {step_total:.3f}s (Upload: {upload_latency:.3f}s | Exec: {exec_latency:.3f}s)")
        
        return observation, reward, done

    def cleanup(self):
        """
        Releases the sandbox back to the cluster.
        """
        # Calculate how long the sandbox was held by this actor
        lifespan = time.time() - self.t_adopted
        
        t_cleanup_start = time.time()
        
        self.client.delete_sandbox(self.sandbox.claim_name)
        cleanup_latency = time.time() - t_cleanup_start
        
        print(f"[{self.env_id}] Sandbox held for {lifespan:.2f}s total. Recycled back to pool in {cleanup_latency:.3f}s")

@ray.remote
class RolloutWorkerActor:
    """
    A trusted Ray Rollout Worker coordinating parallel environment sandboxes.
    This actor scales horizontally across the K8s cluster.
    """
    def __init__(self, worker_id: int, envs_per_worker: int):
        self.worker_id = worker_id
        
        # Initialize multiple remote secure environments for parallel rollout collection
        self.envs = [
            SecureEnvironmentActor.remote(env_id=f"env-sandbox-w{worker_id}-e{i}")
            for i in range(envs_per_worker)
        ]
        self.policy_weights = None

    def sync_weights(self, weights):
        """Receives updated neural network weights from the central trainer."""
        self.policy_weights = weights

    def collect_rollouts(self, num_steps: int):
        """Gathers RL trajectories by stepping the environments in parallel."""
        rollout_data = []
        for step in range(num_steps):
            # Agent's policy network decides actions based on current weights
            actions = [1 if random.random() > 0.5 else 0 for _ in range(len(self.envs))]
            
            # Fire the execution commands to all gVisor sandboxes simultaneously using Ray futures
            step_futures = [
                self.envs[i].step.remote(actions[i])
                for i in range(len(self.envs))
            ]
            
            # Wait for all sandboxes to finish execution and return their stdout
            step_results = ray.get(step_futures)
            
            # Aggregate the trajectories
            for i, (obs, reward, done) in enumerate(step_results):
                rollout_data.append((obs, actions[i], reward))
                
            # Truncate episode if any environment hits a terminal state
            if any(res[2] for res in step_results):
                break
        return rollout_data

    def shutdown_envs(self):
        """Commands all owned sandboxes to run their cleanup logic."""
        cleanup_futures = [env.cleanup.remote() for env in self.envs]
        ray.get(cleanup_futures)

@ray.remote
class RLPolicyTrainer:
    """
    The centralized policy weight trainer. This typically runs on the Ray Head node.
    """
    def __init__(self, num_workers: int, envs_per_worker: int):
        # Initialize dummy policy network weights
        self.weights = {"layer1": [random.random() for _ in range(10)]}
        
        # Spawn the distributed rollout workers
        self.workers = [
            RolloutWorkerActor.remote(worker_id=i, envs_per_worker=envs_per_worker)
            for i in range(num_workers)
        ]

    def train_episode(self, steps_per_worker: int):
        """Runs a full iteration of Proximal Policy Optimization (PPO) logic."""
        print(f"\n--- Training Episode: Syncing policy weights to {len(self.workers)} workers ---")
        
        # 1. Broadcast the latest weights to all workers
        sync_futures = [w.sync_weights.remote(self.weights) for w in self.workers]
        ray.get(sync_futures)
        
        # 2. Command workers to collect trajectories from their secure sandboxes
        rollout_futures = [w.collect_rollouts.remote(steps_per_worker) for w in self.workers]
        all_rollouts = ray.get(rollout_futures)
        
        # 3. Aggregate data and update global network weights
        total_rewards = 0
        total_steps = 0
        for r in all_rollouts:
            total_steps += len(r)
            total_rewards += sum(step[2] for step in r)
            
        # Simulate gradient descent weight update
        self.weights["layer1"] = [w + 0.01 for w in self.weights["layer1"]]
        print(f"Episode Completed: Processed {total_steps} secure steps. Average reward: {total_rewards / len(self.workers)}")
        
        return total_rewards

    def shutdown_all_workers(self):
        """Gracefully tears down the distributed cluster."""
        shutdown_futures = [w.shutdown_envs.remote() for w in self.workers]
        ray.get(shutdown_futures)

def main():
    # Define the scale of the distributed job.
    # 3 workers x 2 envs = 6 total gVisor pods claimed instantly from the WarmPool.
    num_workers = 3
    envs_per_worker = 2
    
    print(f"\nDeploying Scaled RL Policy Trainer on GKE...")
    print(f"Orchestrating {num_workers} workers and {num_workers * envs_per_worker} secure sandboxes E2E!")
    
    trainer = RLPolicyTrainer.remote(num_workers=num_workers, envs_per_worker=envs_per_worker)
    
    # Run the main Policy Iteration loop
    for ep in range(1, 4):
        future_train = trainer.train_episode.remote(steps_per_worker=10)
        ray.get(future_train)
        
    # Ensure all K8s SandboxClaims are deleted upon completion
    ray.get(trainer.shutdown_all_workers.remote())
    ray.shutdown()
    print("\nDistributed Sandbox Training finished successfully E2E!")

if __name__ == "__main__":
    main()